public class Bitwise {
    public static void main(String[] args) {
        int a=5;
        int b=2;
         System.out.println("GREATER: " + (a > b));
        System.out.println("LESSER: " + (a | b));
        System.out.println("GREATER OR EQUAL " + (a^b));
        System.out.println("NOT"+(~a));
    }
}
